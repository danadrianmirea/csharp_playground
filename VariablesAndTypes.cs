using System;

// Define a class named DataTypesDemo
class DataTypesDemo
{
    // Define a demo function
    public void Demo()
    {
        // Declare and initialize integer variable
        int myInt = 10;
        Console.WriteLine("Integer: " + myInt);

        // Declare and initialize floating-point variable
        float myFloat = 3.14f;
        Console.WriteLine("Float: " + myFloat);

        // Declare and initialize double variable
        double myDouble = 2.71828;
        Console.WriteLine("Double: " + myDouble);

        // Declare and initialize character variable
        char myChar = 'A';
        Console.WriteLine("Character: " + myChar);

        // Declare and initialize boolean variable
        bool myBool = true;
        Console.WriteLine("Boolean: " + myBool);

        // Declare and initialize string variable
        string myString = "Hello, C#";
        Console.WriteLine("String: " + myString);

        // Declare and initialize decimal variable
        decimal myDecimal = 123.456m;
        Console.WriteLine("Decimal: " + myDecimal);

        // Declare and initialize byte variable
        byte myByte = 255;
        Console.WriteLine("Byte: " + myByte);

        // Declare and initialize short variable
        short myShort = -32768;
        Console.WriteLine("Short: " + myShort);

        // Declare and initialize long variable
        long myLong = 9223372036854775807L;
        Console.WriteLine("Long: " + myLong);
    }
}